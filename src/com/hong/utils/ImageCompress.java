	/**
	 * 指定大小进行缩放
	 * 
	 * @throws IOException
	 */
	private void test1() throws IOException {
		/*
		 * size(width,height) 若图片横比200小，高比300小，不变
		 * 若图片横比200小，高比300大，高缩小到300，图片比例不变 若图片横比200大，高比300小，横缩小到200，图片比例不变
		 * 若图片横比200大，高比300大，图片按比例缩小，横为200或高为300
		 */
		Thumbnails.of("C:/Users/3hhws/Desktop/Jellyfish_看图王.bmp").size(200, 300).toFile(
				"C:/Users/3hhws/Desktop/image_200x300.jpg");
		Thumbnails.of("C:/Users/3hhws/Desktop/Jellyfish_看图王.bmp").size(2560, 2048).toFile(
				"C:/Users/3hhws/Desktop/image_2560x2048.jpg");
	}

